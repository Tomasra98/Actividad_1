#Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división

num1 = int(input("Digite un número: "))
num2 = int(input("Digite otro número: "))

print("Suma = ", num1 + num2)
print("Resta = ", num1 - num2)
print("Multiplicación = ", num1 * num2)
print("División Exacta = ", num1 / num2)
print("División Inexacta = ", num1 // num2)